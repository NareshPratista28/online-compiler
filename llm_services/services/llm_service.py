    def _parse_response(self, response: str) -> Dict[str, str]:
        """Parse LLM response to extract components"""
        result = {
            "raw_response": response,
            "code": "",
            "studi_kasus": "",
            "tugas": ""
        }
        
        # Extract Java code
        code_pattern = r"```java(.*?)```"
        code_matches = re.findall(code_pattern, response, re.DOTALL)
        if code_matches:
            result["code"] = code_matches[0].strip()
        
        # Extract Studi Kasus
        studi_kasus_pattern = r"\*\*Studi Kasus:\*\*(.*?)(?=\*\*Tugas:\*\*|\Z)"
        studi_kasus_matches = re.findall(studi_kasus_pattern, response, re.DOTALL)
        if studi_kasus_matches:
            result["studi_kasus"] = studi_kasus_matches[0].strip()
        
        # Extract Tugas
        tugas_pattern = r"\*\*Tugas:\*\*(.*?)(?=```|$)"
        tugas_matches = re.findall(tugas_pattern, response, re.DOTALL)
        if tugas_matches:
            result["tugas"] = tugas_matches[0].strip()

        return result

    def _generate_test_cases(self, basic_result: Dict[str, str]) -> list:
        """Generate IO test cases based on the problem description and code"""
        try:
            self.logger.info("Generating IO test cases using LLM...")
            
            code = basic_result.get("code", "")
            studi_kasus = basic_result.get("studi_kasus", "")
            tugas = basic_result.get("tugas", "")
            
            prompt = f"""
            You are a QA Engineer creating test cases for a Java program.
            
            **Case Study:**
            {studi_kasus}
            
            **Student Task:**
            {tugas}
            
            **Code Structure:**
            ```java
            {code}
            ```
            
            Generate 1 simple test case to verify the program runs.
            
            Return ONLY a valid JSON array of objects with these fields:
            - `input`: MUST BE EMPTY STRING ""
            - `expected_output`: "Output" (Generic placeholder, we only check for successful execution)
            - `description`: "Verify program runs successfully without error"
            
            JSON Format Example:
            [
                {"input": "", "expected_output": "Output", "description": "Verify program runs successfully"}
            ]
            
            STRICTLY RETURN JSON ARRAY ONLY. NO MARKDOWN.
            """
            
            llm = self.get_llm_instance()
            response = llm.invoke(prompt)
            
            # Clean response to ensure valid JSON
            json_str = response.strip()
            if "```json" in json_str:
                json_str = json_str.split("```json")[1].split("```")[0].strip()
            elif "```" in json_str:
                json_str = json_str.split("```")[1].split("```")[0].strip()
                
            import json
            test_cases = json.loads(json_str)
            self.logger.info(f"Generated {len(test_cases)} Static test cases")
            return test_cases
            
        except Exception as e:
            self.logger.error(f"Error generating IO test cases: {str(e)}")
            # Fallback test cases
            return [
                {
                    "input": "0",
                    "expected_output": "Output", 
                    "description": "Basic execution test"
                }
            ]

    def _generate_junit_tests(self, basic_result: Dict[str, str], test_cases: list) -> str:
        """Generate JUnit test code menggunakan LLM prompt"""
        try:
            self.logger.info("🧪 Generating JUnit tests using LLM prompt...")
            
            code = basic_result.get("code", "")
            studi_kasus = basic_result.get("studi_kasus", "")
            tugas = basic_result.get("tugas", "")
            
            # Extract class name
            class_match = re.search(r'class\s+(\w+)', code)
            class_name = class_match.group(1) if class_match else "Solution"
            
            # Create JUnit generation prompt using FEW-SHOT strategy
            junit_prompt = self._get_junit_prompt(code, studi_kasus, tugas, class_name, test_cases)
            
            # Generate JUnit using LLM
            llm = self.get_llm_instance()
            
            llm_start_time = time.time()
            response = llm.invoke(junit_prompt)
            llm_generation_time = time.time() - llm_start_time
            
            self.logger.info(f"JUnit LLM response dalam {llm_generation_time:.2f} detik")
            
            # Parse dan validate response
            junit_code = self._parse_junit_response(response, class_name)
            
            self.logger.info("✅ JUnit test berhasil dihasilkan dengan LLM")
            return junit_code
            
        except Exception as e:
            self.logger.error(f"Error generating JUnit tests with LLM: {str(e)}")
            # Fallback to simple template if LLM fails
            return self._generate_fallback_junit_template(class_name)
    
