    def _parse_junit_response(self, response: str, class_name: str) -> str:
        """Parse and validate LLM response for JUnit code"""
        try:
            # Extract Java code from response if wrapped in markdown
            junit_code = response
            
            # Remove markdown code blocks if present
            if "```java" in junit_code:
                junit_code = junit_code.split("```java")[1].split("```")[0].strip()
            elif "```" in junit_code:
                junit_code = junit_code.split("```")[1].split("```")[0].strip()
            
            # Validate that it contains expected JUnit elements
            required_elements = [
                f"class JUnit{class_name}Test",
                "@Test",
                "import org.junit",
                "package {{{{user_package}}}}"
            ]
            
            for element in required_elements:
                if element not in junit_code:
                    self.logger.warning(f"Missing required element in JUnit code: {element}")
            
            # Basic syntax validation
            if not junit_code.strip():
                raise ValueError("Empty JUnit code generated")
            
            self.logger.info("JUnit code validation passed")
            return junit_code
            
        except Exception as e:
            self.logger.error(f"Error parsing JUnit response: {str(e)}")
            # Return fallback template
            return self._generate_fallback_junit_template(class_name)

