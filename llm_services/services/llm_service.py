    def _validate_response(self, result: Dict[str, str]) -> bool:
        """Validates the quality of the generated response."""
        # Check for minimum content length
        if len(result.get("studi_kasus", "")) < 30:
            return False
            
        # Check that code contains expected placeholders
        if "..." not in result.get("code", ""):
            return False
            
        # Check that tasks are in bullet point format
        if not re.search(r"- .+", result.get("tugas", "")):
            return False
            
        return True

    def generate_question_with_tests(self, content_id: int) -> Tuple[Dict[str, Any], float, int]:
        """
        Generate pertanyaan lengkap dengan test cases dan kode JUnit
        
        Args:
            content_id: ID konten pembelajaran
              
        Returns:
            Tuple berisi hasil, waktu generasi, dan history ID
        """
        start_time = time.time()
        
        try:
            self.logger.info(f"===== MEMULAI GENERASI PERTANYAAN DENGAN TESTS (ID: {content_id}) =====")
            
            # Mendapatkan prompt dari model content dengan lazy import
            self.logger.info(f"[1/5] Mengambil prompt_llm dan konten dari database (content_id: {content_id})")
            
            # Lazy import to avoid AppRegistryNotReady error
            from ..models.content import ContentModel
            content_model = ContentModel()
            result = content_model.get_prompt_and_description(content_id)
            
            if not result:
                self.logger.error(f"Content tidak ditemukan untuk content_id: {content_id}")
                raise ValueError(f"Content tidak ditemukan untuk content_id: {content_id}")
            
            prompt_llm = result.get('prompt_llm') or ''
            content_text = result.get('description') or ''
            content_title = result.get('title') or ''

            # Log panjang konten untuk debugging
            self.logger.info(f"Panjang prompt_llm: {len(prompt_llm)} karakter")
            self.logger.info(f"Panjang content_text: {len(content_text)} karakter")
            self.logger.info(f"Judul materi: {content_title}")
            
            # Ekstrak task_type dari konten daripada memilih secara acak
            self.logger.info(f"[2/5] Menggunakan judul materi sebagai task_type: '{content_title}'")
            self.logger.info(f"[3/5] Menyusun prompt lengkap dengan RAG examples dan materi pembelajaran")
            full_prompt = self.create_prompt(prompt_llm, content_text, content_title)
            self.logger.info(f"Prompt lengkap berhasil dibuat: {len(full_prompt)} karakter")
            
            # Mendapatkan instance LLM
            self.logger.info(f"[4/5] Mengirim prompt ke model LLM {self.model_name}")
            llm = self.get_llm_instance()
            
            llm_start_time = time.time()
            response = llm.invoke(full_prompt)
            llm_generation_time = time.time() - llm_start_time
            
            self.logger.info(f"LLM response dalam {llm_generation_time:.2f} detik")
            
            
            basic_result = self._parse_response(response)
            
            # [5/5] Generate IO Test Cases (No JUnit)
            self.logger.info("[5/5] Generating IO test cases with LLM")
            test_cases = self._generate_test_cases(basic_result)
            
            # Set empty junit code
            junit_code = ""
            
            # Gabungkan hasil lengkap
            complete_result = {
                **basic_result,
                "test_cases": test_cases,
                "junit_test_code": junit_code,
                "solution_template": basic_result.get('code', ''),
                "metadata": {
                    'content_id': content_id,
                    'content_title': content_title,
                    'generation_time': llm_generation_time,
                    'refactored': False,  
                    'junit_approach': 'io_based_grading',  # Updated to IO based grading
                    'prompting_strategy': 'io_test_generation'
                }
            }
            
            generation_time = time.time() - start_time
            
            # Simpan ke history
            history_id = 0
            try:
                history_model = self._get_history_model()
                if history_model:
                    history_id = history_model.save_generation(
                        content_id=content_id,
                        topic_title=content_title,
                        result=complete_result,
                        generation_time=generation_time
                    )
                    self.logger.info(f"History saved with ID: {history_id}")
                else:
                    self.logger.warning("History model not available, skipping history save")
            except Exception as e:
                self.logger.error(f"Error saving history: {e}")
            
            self.logger.info(f"===== GENERASI SELESAI dalam {generation_time:.2f}s =====")
            return complete_result, generation_time, history_id
            
        except Exception as e:
            execution_time = time.time() - start_time
            self.logger.error(f"Error generating question: {str(e)}", exc_info=True)
            
            # Return fallback result
            error_result = self._create_fallback_complete_result(str(e))
            return error_result, execution_time, 0
        
